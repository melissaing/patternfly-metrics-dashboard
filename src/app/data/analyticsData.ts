export interface DesignSystemTrendsData {
  patternfly: Array<{ x: string; y: number }>;
  shopify: Array<{ x: string; y: number }>;
  carbon: Array<{ x: string; y: number }>;
  atlaskit: Array<{ x: string; y: number }>;
}

/**
 * NPM Trends Data Service
 * 
 * Fetches live download statistics from the NPM Registry API to compare
 * design system package popularity. The service automatically handles:
 * - CORS issues by using the public npm registry API
 * - Error handling with graceful fallback to static data
 * - Data transformation for chart compatibility
 * - Rate limiting and caching considerations
 * 
 * Usage:
 * ```typescript
 * const data = await NpmTrendsService.fetchTrendsData();
 * ```
 * 
 * Data source: https://api.npmjs.org/downloads/range
 * Update frequency: Recommended every 30 minutes to respect API limits
 * Packages tracked: @patternfly/react-core, @shopify/polaris, @carbon/react, @atlaskit/button
 */
export class NpmTrendsService {
  private static readonly NPM_API_BASE = 'https://api.npmjs.org/downloads/range';
  private static readonly PACKAGES = [
    '@patternfly/react-core',
    '@shopify/polaris', 
    '@carbon/react',
    '@atlaskit/button'
  ];

  static async fetchTrendsData(): Promise<DesignSystemTrendsData> {
    try {
      // Use npm registry API directly to avoid CORS issues
      const endDate = new Date();
      const startDate = new Date();
      startDate.setMonth(startDate.getMonth() - 12);
      
      const startDateStr = startDate.toISOString().split('T')[0];
      const endDateStr = endDate.toISOString().split('T')[0];
      
      // Fetch data for each package separately
      const promises = this.PACKAGES.map(async (packageName) => {
        const url = `${this.NPM_API_BASE}/${startDateStr}:${endDateStr}/${packageName}`;
        
        try {
          const response = await fetch(url);
          if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
          }
          const data = await response.json();
          return { packageName, data: data.downloads || [] };
        } catch (error) {
          console.warn(`Failed to fetch data for ${packageName}:`, error);
          return { packageName, data: [] };
        }
      });
      
      const results = await Promise.all(promises);
      return this.transformNpmApiData(results);
      
    } catch (error) {
      console.error('Error fetching NPM data:', error);
      // Return fallback data on error
      return this.getFallbackData();
    }
  }

  private static transformNpmApiData(results: Array<{ packageName: string; data: Array<{ day: string; downloads: number }> }>): DesignSystemTrendsData {
    const monthlyData: { [key: string]: { [packageName: string]: number } } = {};
    
    // Process each package's download data
    results.forEach(({ packageName, data }) => {
      data.forEach(({ day, downloads }) => {
        const monthKey = day.substring(0, 7); // YYYY-MM format
        
        if (!monthlyData[monthKey]) {
          monthlyData[monthKey] = {};
        }
        
        if (!monthlyData[monthKey][packageName]) {
          monthlyData[monthKey][packageName] = 0;
        }
        
        monthlyData[monthKey][packageName] += downloads;
      });
    });

    // Convert to chart format and sort months
    const months = Object.keys(monthlyData).sort();
    
    return {
      patternfly: months.map(month => ({
        x: month,
        y: monthlyData[month]['@patternfly/react-core'] || 0
      })),
      shopify: months.map(month => ({
        x: month,
        y: monthlyData[month]['@shopify/polaris'] || 0
      })),
      carbon: months.map(month => ({
        x: month,
        y: monthlyData[month]['@carbon/react'] || 0
      })),
      atlaskit: months.map(month => ({
        x: month,
        y: monthlyData[month]['@atlaskit/button'] || 0
      }))
    };
  }

  private static getFallbackData(): DesignSystemTrendsData {
    // Return the existing static data as fallback
    return designSystemData;
  }
} 